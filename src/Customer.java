public class Customer {

    //Nahom lists the customers input data types
    private String name;
    private String address;
    private int age;

    //Nahom lists the customers Constructor parameters
    public Customer(String name, String address, int age) {
        this.name = name;
        this.address = address;
        this.age = age;
    }
    //Getter and Setter for the customer constructor parameters
    public String getName() {
        return name;
    }


}
